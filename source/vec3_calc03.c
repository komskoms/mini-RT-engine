#include "vector3.h"
#include <math.h>

double			vec3_calc_dist(t_vec pa[3], t_vec pb[3])
{
	return (sqrt((pb[0] - pa[0]) * (pb[0] - pa[0])
				+ (pb[1] - pa[1]) * (pb[1] - pa[1])
				+ (pb[2] - pa[2]) * (pb[2] - pa[2])));
}
