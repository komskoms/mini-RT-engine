#include "libft.h"

int		ft_isdigit(int c)
{
	if (c >= '0' && c <= '9')
		return (1);
	else
		return (0);
}

int		ft_isalnum(int c)
{
	return ((ft_isdigit(c) != 0) || (ft_isalpha(c) != 0));
}
